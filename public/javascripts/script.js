function changeImage() {

    if (document.getElementById("imgClickAndChange").src == "https://i.ytimg.com/vi/zGYsq7cbVZA/hqdefault.jpg") 
    {
        document.getElementById("imgClickAndChange").src = "https://i.ytimg.com/vi/ZNhL7MoSahQ/hqdefault.jpg";
    }
    else 
    {
        document.getElementById("imgClickAndChange").src = "https://i.ytimg.com/vi/zGYsq7cbVZA/hqdefault.jpg";
    }
}